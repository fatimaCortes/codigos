# Programa que pida un número y diga si es positivo, negativo o 0.
#################################################################################

print ("\nPrograma que valida si un numero es positivo o negativo")


num = int (input ("\nIngresa un número: "))

if num >0:
    print ("El numero es positivo")
elif num <0:
    print ("El numero es negativo")
else: 
    print ("Es 0")
print("\nfin de programa")


